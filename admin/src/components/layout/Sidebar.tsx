export function Sidebar({ userRole }: { userRole: Role }) {
  const pathname = usePathname();
  const visible = MENU_ITEMS.filter((item) => item.check(userRole));

  return (
    <aside className="fixed left-0 top-0 z-40 h-screen w-64 border-r border-gray-200 bg-white">
      <div className="flex h-14 items-center border-b border-gray-200 px-4">
        <span className="text-lg font-semibold text-gray-900">Cardápio Admin</span>
      </div>
      <nav className="space-y-0.5 p-2">
        {visible.map((item) => {
          const key = item.path.split('/')[1] ?? 'dashboard';
          const isActive = pathname === item.path || pathname.startsWith(item.path + '/');
          return (
            <Link
              key={item.path}
              href={item.path}
              className={cn(
                'flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                isActive ? 'bg-primary/10 text-primary' : 'text-gray-700 hover:bg-gray-100'
              )}
            >
              <svg className="h-5 w-5 shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={icons[key] ?? icons.dashboard} />
              </svg>
              {item.label}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}
